#include <stddef.h>

void	*ft_bzero(void *s, size_t n)
{
	char	*s2;
	size_t	i;

	s2 = s;
	i = 0;
	while (i < n)
	{
		*s2 = '\0';
		s2++;
		i++;
	}
	return (s);
}
